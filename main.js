function isValidKeyFormat(key) {
  if (typeof key !== "string")
    return false;
  const trimmed = key.trim();
  if (trimmed.length < 10 || trimmed.length > 128)
    return false;
  if (/\s/.test(trimmed))
    return false;
  const vtFormat = /^VT-[0-9A-F]{4}-[0-9A-F]{4}-[0-9A-F]{4}-[0-9A-F]{4}$/i;
  const uuidFormat = /^[0-9A-F]{8}-[0-9A-F]{4}-[0-9A-F]{4}-[0-9A-F]{4}-[0-9A-F]{12}$/i;
  return vtFormat.test(trimmed) || uuidFormat.test(trimmed);
}

