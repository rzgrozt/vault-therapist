var OllamaProvider = class extends BaseAIProvider {
  constructor(baseUrl, model, embeddingModel) {
    super();
    this.name = "Ollama";
    this.supportsNativeEmbeddings = true;
    /** Whether the new /api/embed endpoint is available (detected on first use). */
    this.embedEndpointIsNew = null;
    this.baseUrl = baseUrl.replace(/\/$/, "");
    this.model = model;
    this.embeddingModel = embeddingModel;
  }
  // ── Completions ──────────────────────────────────────────────────
  async complete(prompt, systemPrompt) {
    const messages = this.buildMessages(prompt, systemPrompt);
    const response = await fetch(`${this.baseUrl}/api/chat`, {
      method: "POST",
      headers: { "Content-Type": "application/json" },
      body: JSON.stringify({ model: this.model, messages, stream: false }),
      signal: AbortSignal.timeout(NETWORK_TIMEOUT_MS)
    });
    if (!response.ok) {
      throw new Error(`Ollama chat error ${response.status}: ${await response.text()}`);
    }
    const data = await response.json();
    return data.message.content;
  }
  // ── Single embed (delegates to batch) ────────────────────────────
  async embed(text) {
    const [vector] = await this.embedBatch([text]);
    return vector;
  }
  // ── Batch embed ──────────────────────────────────────────────────
  async embedBatch(texts) {
    if (this.embedEndpointIsNew === null) {
      this.embedEndpointIsNew = await this.detectEmbedEndpoint();
    }
    if (this.embedEndpointIsNew) {
      return this.embedBatchNew(texts);
    }
    return this.embedBatchLegacy(texts);
  }
  // ── Connection test ──────────────────────────────────────────────
  async testConnection() {
    try {
      const response = await fetch(`${this.baseUrl}/api/tags`, {
        method: "GET",
        signal: AbortSignal.timeout(3e3)
      });
      return response.ok;
    } catch (e) {
      return false;
    }
  }
  // ── Private helpers ──────────────────────────────────────────────
  async detectEmbedEndpoint() {
    try {
      const response = await fetch(`${this.baseUrl}/api/embed`, {
        method: "POST",
        headers: { "Content-Type": "application/json" },
        body: JSON.stringify({ model: this.embeddingModel, input: "probe" })
      });
      if (response.ok)
        return true;
      if (response.status === 404)
        return false;
      return true;
    } catch (e) {
      return false;
    }
  }
  async embedBatchNew(texts) {
    const response = await fetch(`${this.baseUrl}/api/embed`, {
      method: "POST",
      headers: { "Content-Type": "application/json" },
      body: JSON.stringify({ model: this.embeddingModel, input: texts }),
      signal: AbortSignal.timeout(NETWORK_TIMEOUT_MS)
    });
    if (!response.ok) {
      throw new Error(`Ollama embed error ${response.status}: ${await response.text()}`);
    }
    const data = await response.json();
    return data.embeddings;
  }
  async embedBatchLegacy(texts) {
    const results = [];
    for (const text of texts) {
      const response = await fetch(`${this.baseUrl}/api/embeddings`, {
        method: "POST",
        headers: { "Content-Type": "application/json" },
        body: JSON.stringify({ model: this.embeddingModel, prompt: text }),
        signal: AbortSignal.timeout(NETWORK_TIMEOUT_MS)
      });
      if (!response.ok) {
        throw new Error(`Ollama embed error ${response.status}: ${await response.text()}`);
      }
      const data = await response.json();
      results.push(data.embedding);
    }
    return results;
  }
};

